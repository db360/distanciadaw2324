/**
 * Función para sumar dos números.
 *
 * Esta función toma dos números como entrada y devuelve su suma.
 *
 * @param int $num1 El primer número a sumar.
 * @param int $num2 El segundo número a sumar.
 * @return int La suma de $num1 y $num2.
 * @since 1.0
 */
function sumar($num1, $num2) {
    return $num1 + $num2;
}

/**
 * Función para multiplicar dos números.
 *
 * Esta función toma dos números como entrada y devuelve su producto.
 *
 * @param int $num1 El primer número a multiplicar.
 * @param int $num2 El segundo número a multiplicar.
 * @return int El producto de $num1 y $num2.
 * @since 1.0
 */
function multiplicar($num1, $num2) {
    return $num1 * $num2;
}

